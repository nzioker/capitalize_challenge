let randomNumberOne = Math.floor(Math.random() * 100 + 1);

let randomNumberTwo = Math.floor(Math.random() * 50 + 1);

console.log(
  `${randomNumberOne} + ${randomNumberTwo} = ${
    randomNumberOne + randomNumberTwo
  }`
);
console.log(
  `${randomNumberOne} - ${randomNumberTwo} = ${
    randomNumberOne - randomNumberTwo
  }`
);
console.log(
  `${randomNumberOne} * ${randomNumberTwo} = ${
    randomNumberOne * randomNumberTwo
  }`
);
console.log(
  `${randomNumberOne} / ${randomNumberTwo} = ${
    randomNumberOne / randomNumberTwo
  }`
);
console.log(
  `${randomNumberOne} % ${randomNumberTwo} = ${
    randomNumberOne % randomNumberTwo
  }`
);
